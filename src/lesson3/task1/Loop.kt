    var lessx = x
    while(abs(lessx) > 2 * PI) {
        if (lessx > 0) {
            lessx -= 2 * PI
        }
        if (lessx < 0) {
            lessx += 2 * PI
        }
    }
    var x0 = x
    var elem = 3
    var sum = 0.0
    while (abs(x0) >= eps) {
        sum += x0
        x0 = -x0 * x * x / (elem * (elem - 1))
        elem += 2
    }
    return sum
}

